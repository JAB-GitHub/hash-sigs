/*
 * This tests out the SHAKE-based parameter sets
 * Actually, the other tests exercise the SHAKE based parameter sets pretty
 * thoroughly.  This is here mostly because of the check (so that the user is
 * told whether we believe that SHAKE is supported or not); while we're here,
 * we might as well test out the low-level SHAKE hash routine to see if it's
 * right (we don't have any other verification of that)
 */
#include <stdio.h>
#include <string.h>
#include "test_hss.h"

bool check_shake(bool fast_flag) {
    if (test_if_sha3_is_supported()) {
        return true;
    }
    printf( "  Support for SHAKE not enabled - test skipped\n" );

    return false;
}

/*
 * Normally, we try to test only the explicit API.  However, we'd also like
 * to KAT the underlying SHAKE implementation, and the standard API doesn't
 * give us access to that.  So, we cheat
 */
#include "hash.h"

#define MAGIC_VALUE 42   /* Value we use to verify that the hash doesn't */
                         /* overwrite parts of the buffer it shouldn't */
                         /* The IETF official random number */
static bool check_output_buffer( const unsigned char *output_buffer,
                                 const unsigned char *expected_hash,
                                 unsigned hash_len ) {
    unsigned char what_it_should_be[ 32+8 ];
    memset( what_it_should_be, MAGIC_VALUE, sizeof what_it_should_be );
    memcpy( what_it_should_be+4, expected_hash, hash_len );

    if (0 != memcmp( output_buffer, what_it_should_be, 32+8 )) return false;

    return true;
}

/*
 * This makes sure that the hash of the message is the expected value
 */
static bool test_hash( int hash_type, const unsigned char *message, unsigned msg_len,
                       const unsigned char *expected_hash, unsigned hash_len ) {
    unsigned char output_buffer[ 32+8 ];
    memset( output_buffer, MAGIC_VALUE, sizeof output_buffer );

    hss_hash(output_buffer+4, hash_type, message, msg_len );

    if (!check_output_buffer( output_buffer, expected_hash, hash_len )) return false;

    /* Try incremental hashing next, dividing the message into two different ways */
    union hash_context ctx;
    int i;
    unsigned incr = msg_len/4;
    for (i=0; i<2; i++) {
        memset( output_buffer, MAGIC_VALUE, sizeof output_buffer );

        hss_init_hash_context( hash_type, &ctx );

        unsigned offset = 0;
        while (offset < msg_len) {
            unsigned end = offset + incr;
            if (end > msg_len) end = msg_len;
            hss_update_hash_context( hash_type, &ctx, &message[offset], end - offset );

            offset = end;
            incr = msg_len/2 + 1;
        }
        hss_finalize_hash_context( hash_type, &ctx, output_buffer+4 );

        if (!check_output_buffer( output_buffer, expected_hash, hash_len )) return false;
    }

    return true;
}

